const maxProduct = function (nums) {
  nums.sort((a, b) => a - b);
  return nums.slice(-2).reduce((a, b) => (a - 1) * (b - 1));
};
const input = [1, 8, 5, 4, 10, 2, 6, 1, 1, 1, 9];
console.log(maxProduct(input));
console.log(maxProduct([1, 5, 4, 5]));
console.log(maxProduct([10, 2, 5, 2]));
